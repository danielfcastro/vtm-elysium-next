export function getSpecialtiesForTrait(
  category: "attributes" | "abilities",
  traitCategory: string,
  traitId: string,
  isLegendary = false,
): SpecialtyItem[] {
  const getItems = (items: string[]): SpecialtyItem[] =>
    items.map((name) => ({ name }));

  // Normalize to lowercase for lookup
  const normCat = traitCategory.toLowerCase();
  // Normalize: underscores to spaces, and lowercase
  const normId = traitId.toLowerCase().replace(/_/g, " ");
  const normIsLegendary = isLegendary;

  if (category === "attributes") {
    // Map lowercase category to actual key
    const catMap: Record<string, keyof typeof SPECIALTY_DATA.attributes> = {
      physical: "Physical",
      social: "Social",
      mental: "Mental",
    };
    const actualCat = catMap[normCat];
    if (!actualCat) return [];

    // Find trait in the category (case-insensitive)
    const attrs = SPECIALTY_DATA.attributes[actualCat];
    const traitKey = Object.keys(attrs).find(
      (k) => k.toLowerCase() === normId,
    ) as keyof typeof attrs | undefined;

    if (!traitKey) return [];
    const attrData = attrs[traitKey];

    // At level 5+, show both regular AND legendary combined
    let items: string[];
    if (normIsLegendary && attrData.legendary) {
      items = [...attrData.regular, ...attrData.legendary];
    } else {
      items = attrData.regular;
    }
    return getItems(items);
  }

  // Abilities - normalize "knowledges" -> "knowledge"
  const abilityCatMap: Record<string, keyof typeof SPECIALTY_DATA.abilities> = {
    talents: "Talents",
    skills: "Skills",
    knowledges: "Knowledge",
    knowledge: "Knowledge",
  };
  const actualAbilCat = abilityCatMap[normCat];
  if (!actualAbilCat) return [];

  const abilities = SPECIALTY_DATA.abilities[actualAbilCat];
  const traitKey = Object.keys(abilities).find(
    (k) => k.toLowerCase() === normId,
  ) as keyof typeof abilities | undefined;

  if (!traitKey) return [];
  const abilityData = abilities[traitKey];

  // At level 5+, show both regular AND legendary combined
  let items: string[];
  if (normIsLegendary && abilityData.legendary) {
    items = [...abilityData.regular, ...abilityData.legendary];
  } else {
    items = abilityData.regular;
  }
  return getItems(items);
}

export function isLegendaryRating(rating: number): boolean {
  return rating >= 5;
}
